function checkAnswer(e){
	if(game.checked==false){
		game.audio.volume = 0.5;
		game.time=-1;
		game.checked = true;
		var result = $(e).text();
		$(e).css("background","green");
		result = result.slice(3,result.length);
		if(result==game.bank[game.index].answer[0]){
			game.question++;
			setTimeout(function(){
				var b = 0;
				game.audioTrue.load();
				game.audioTrue.play();
				var a =setInterval(function(){
						if(b == 0){
							$(e).css("background","yellow");
							b=1;
						}
						else{
							$(e).css("background","green");
							b=0;
						}
					 },200);
				setTimeout(function(){
					clearInterval(a);
					$(e).css("background","#1E88E5");
					game.play();
				},2000);
			},2000);
		}
		else{
			var r = $('[value="'+game.ar+'"]');
			setTimeout(function(){
				var b = 0;
				game.audioFalse.load();
				game.audioFalse.play();
				var a =setInterval(function(){
						if(b == 0){
							$(r).css("background","yellow");
							b=1;
						}
						else{
							$(r).css("background","#1E88E5");
							b=0;
						}
					 },200);
				setTimeout(function(){
					clearInterval(a);
					$(e).css("background","#1E88E5");
					gameOver();
				},2000);
			},2000);
		}
	}
}
